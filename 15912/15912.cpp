#include <iostream>

using namespace std;

int n;
long long w[1010];
long long e[1010];
long long d[1010][1010];

long long we(int start, int end) {
	long long w_max = 0, e_max = 0;
	int i;
	for (i = start; i <= end; i++) {
		if (w_max < w[i]) w_max = w[i];
		if (e_max < e[i]) e_max = e[i];
	}
	return w_max * e_max;
}

int main()
{
	int i, j, k;

	cin >> n;
	for (i = 1; i <= n; i++) {
		cin >> w[i];
	}
	for (i = 1; i <= n; i++) {
		cin >> e[i];
	}

	long long min = 100000000000000;
	for (j = 1; j <= n; j++) {
		d[j][j] = w[j] * e[j];
		for (i = j - 1; i >= 1; i--) {
			min = 100000000000000;
			for (k = i; k < j; k++) {
				if (min > d[i][k] + d[k + 1][j]) {
					min = d[i][k] + d[k + 1][j];
				}
			}
			long long temp = we(i, j);
			if (min > temp) {
				min = temp;
			}
			d[i][j] = min;
		}
	}

	cout << d[1][n];

	return 0;
}

